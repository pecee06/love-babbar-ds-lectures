#include <iostream> // Including a header file which contains predefined ways for console i/o
using namespace std;    // namespace is a concept in C++ used to declare a domain for variables, functions, classes. By using namespace std, we're telling the compiler to use the standard domain where all the standard features of C++ are included

int main(int argc, char const *argv[])
{
    // main() in the starting point of the program, only the code written within main() gets executed
    cout<<"Hello World"<<endl;  // cout is used for console output, endl means a newline
    // To concatenate variables, methods and strings we use "<<" in C++
    
    return 0;
}
